lst = [1, 2, 3, 4, 5, 6]

# Випадок 1: список порожній
if len(lst) == 0:
    result = [[], []]

else:
    # Обчислюємо середину
    mid = len(lst) // 2

    # Якщо кількість елементів непарна — перший список має бути більшим
    if len(lst) % 2 != 0:
        mid += 1

    # Формуємо результат
    first_part = lst[:mid]
    second_part = lst[mid:]
    result = [first_part, second_part]

print(result)

